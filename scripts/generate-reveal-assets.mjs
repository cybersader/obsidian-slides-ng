// Pre-build step: read reveal.js + reveal.css + the bundled themes from
// node_modules and emit a generated TypeScript module exporting them as
// string constants. The renderer then interpolates these strings into the
// iframe-srcdoc HTML at runtime.
//
// We do this at build time (rather than `fs.readFileSync` at runtime in the
// plugin) because the shipped plugin folder only contains main.js +
// manifest.json + styles.css — no node_modules. Build-time inlining is the
// only way to satisfy the "no external runtime requirement" hard constraint.

import { readFileSync, writeFileSync, mkdirSync, existsSync } from "fs";
import { dirname } from "path";

const OUT = "src/render/revealAssets.generated.ts";

const sources = {
  revealJs: "node_modules/reveal.js/dist/reveal.js",
  revealCss: "node_modules/reveal.js/dist/reveal.css",
  themeBlack: "node_modules/reveal.js/dist/theme/black.css",
  themeWhite: "node_modules/reveal.js/dist/theme/white.css",
  themeSimple: "node_modules/reveal.js/dist/theme/simple.css",
};

const banner =
  "// AUTO-GENERATED by scripts/generate-reveal-assets.mjs — do not edit.\n" +
  "// Run `bun run gen:reveal-assets` to refresh.\n\n";

const body = Object.entries(sources)
  .map(([name, path]) => {
    const content = readFileSync(path, "utf-8");
    return `export const ${name} = ${JSON.stringify(content)};\n`;
  })
  .join("\n");

if (!existsSync(dirname(OUT))) {
  mkdirSync(dirname(OUT), { recursive: true });
}

writeFileSync(OUT, banner + body);
console.log(
  `[gen:reveal-assets] wrote ${OUT} (${(banner + body).length} chars, ` +
    Object.keys(sources).length +
    " assets)"
);
