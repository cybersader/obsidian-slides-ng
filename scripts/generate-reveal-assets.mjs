// Pre-build step: read reveal.js + reveal.css + the bundled themes from
// node_modules and emit a generated TypeScript module exporting them as
// string constants. The renderer then interpolates these strings into the
// iframe-srcdoc HTML at runtime.
//
// We do this at build time (rather than `fs.readFileSync` at runtime in the
// plugin) because the shipped plugin folder only contains main.js +
// manifest.json + styles.css — no node_modules. Build-time inlining is the
// only way to satisfy the "no external runtime requirement" hard constraint.

import { readFileSync, writeFileSync, mkdirSync, existsSync, readdirSync } from "fs";
import { dirname, basename } from "path";
import esbuild from "esbuild";

const OUT = "src/render/revealAssets.generated.ts";

const reveal = {
  revealJs: "node_modules/reveal.js/dist/reveal.js",
  revealCss: "node_modules/reveal.js/dist/reveal.css",
};

// Auto-discover every theme CSS in node_modules/reveal.js/dist/theme.
// Each becomes an exported constant `theme<PascalName>`.
const themeDir = "node_modules/reveal.js/dist/theme";
const themeFiles = readdirSync(themeDir)
  .filter((f) => f.endsWith(".css"))
  .sort();

const themes = {};
for (const f of themeFiles) {
  const slug = basename(f, ".css");
  themes[slug] = `${themeDir}/${f}`;
}

const banner =
  "// AUTO-GENERATED by scripts/generate-reveal-assets.mjs — do not edit.\n" +
  "// Run `bun run gen:reveal-assets` to refresh.\n\n";

const exports = [];

// Reveal.js core JS + base CSS.
for (const [name, path] of Object.entries(reveal)) {
  const content = readFileSync(path, "utf-8");
  exports.push(`export const ${name} = ${JSON.stringify(content)};`);
}

// One export per theme + an index map from theme name → CSS content.
const indexEntries = [];
for (const [slug, path] of Object.entries(themes)) {
  const exportName = "theme_" + slug.replace(/[^a-zA-Z0-9_]/g, "_");
  const content = readFileSync(path, "utf-8");
  exports.push(`export const ${exportName} = ${JSON.stringify(content)};`);
  indexEntries.push(`  ${JSON.stringify(slug)}: ${exportName},`);
}

exports.push(
  `\nexport const THEMES_BY_NAME: Record<string, string> = {\n${indexEntries.join("\n")}\n};`
);

// ---------------------------------------------------------------------------
// Bundle shiki-magic-move's vanilla renderer (renderer.mjs + core.mjs +
// diff-match-patch-es + ohash) into a single IIFE that we can drop into
// the iframe srcdoc as a <script>. The runtime exposes window.SlidesNgMagicMove
// = { MagicMoveRenderer, toKeyedTokens }.
// ---------------------------------------------------------------------------
const magicMoveEntry = "scripts/magic-move-bundle-entry.mjs";
if (!existsSync(magicMoveEntry)) {
  writeFileSync(
    magicMoveEntry,
    [
      "// Auto-generated entry — bundled into iframe runtime by",
      "// scripts/generate-reveal-assets.mjs",
      "import { MagicMoveRenderer } from 'shiki-magic-move/renderer';",
      "import { toKeyedTokens } from 'shiki-magic-move/core';",
      "globalThis.SlidesNgMagicMove = { MagicMoveRenderer, toKeyedTokens };",
      "",
    ].join("\n")
  );
}

const mmResult = await esbuild.build({
  entryPoints: [magicMoveEntry],
  bundle: true,
  format: "iife",
  target: "es2018",
  minify: true,
  treeShaking: true,
  write: false,
  logLevel: "silent",
});
const magicMoveJs = mmResult.outputFiles[0].text;
const magicMoveCss = readFileSync(
  "node_modules/shiki-magic-move/dist/style.css",
  "utf-8"
);
exports.push(`export const magicMoveJs = ${JSON.stringify(magicMoveJs)};`);
exports.push(`export const magicMoveCss = ${JSON.stringify(magicMoveCss)};`);

if (!existsSync(dirname(OUT))) {
  mkdirSync(dirname(OUT), { recursive: true });
}

const body = banner + exports.join("\n\n") + "\n";
writeFileSync(OUT, body);

console.log(
  `[gen:reveal-assets] wrote ${OUT} (${body.length} chars, ` +
    (Object.keys(reveal).length + Object.keys(themes).length) +
    ` assets — ${Object.keys(themes).length} themes: ${Object.keys(themes).join(", ")})`
);
